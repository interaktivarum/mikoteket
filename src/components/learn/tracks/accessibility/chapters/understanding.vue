    <h3>Diskriminerande design och teknik</h3>

    <p>
      Vad händer ifall designers och utvecklare inte utgår ifrån användarens behov, ifall förståelse saknas för <i>vem</i> som kommer att använda ett verktyg eller en tjänst, och <i>hur</i>? I bästa fall kommer designen att fungera för samtliga användare, men i värsta fall kan tekniken vara helt obrukbar för vissa personer och upplevas som diskriminerande.
    </p>

