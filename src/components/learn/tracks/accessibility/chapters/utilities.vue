      För att förbereda personer för kommande händelser och besök på nya platser kan <a-wiki c="Panorama_(fotografi)">360-graders panoramafotografi</a-wiki> användas för att skapa sociala berättelser. I bild- och ljudserien nedan används Google Street View och Spotify för att förbereda inför ett besök på Göteborgs konserthus. Serien kan med fördel användas i kombination med konserthusets egen sida på <a href="http://www.t-d.se/sv/TD2/Avtal/Vastra-Gotalandsregionen/Goteborgs-konserthus/Stenhammarsalen/" target="_blank">Tillgänglighetsdatabasen</a>.
