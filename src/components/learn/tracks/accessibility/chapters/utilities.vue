    </p>

    <p>
      Ett liknande svenskt inititiv är <a href="https://www.textamig.se/" target="_blank">Texta mig!</a> som erbjuder ett fritt verktyg för att generera automatiska undertexter till videoklipp, med hjälp av taligenkänning. Vem som helst kan i verktyget ange webbadressen till ett videoklipp på YouTube eller Vimeo och återfå en tidskodad, redigerbar textfil. För YouTubeklipp kan textfilen sedan laddas upp och tillgängliggöras fritt för allmänheten; för Vimeo kan du endast ladda upp textfiler till dina egna videoklipp.
    </p>

    <p>
      <a href="https://www.ribbingreader.com/" target="_blank">Ribbing Reader</a> är ett fritt verktyg (utvecklat av Interaktiva rum som driver Mikoteket) som med Chromes inbyggda taligenkänning erbjuder automatisk transkribering och undertexter av tal, direkt i webbläsaren. Verktyget möjliggör för exempelvis pedagoger att i realtid texttolka (och framöver även spara) det som sägs för elever med nedsatt hörsel eller koncentrationssvårigheter, samt för tillgängliggörande av konferenser, föreläsningar, tv-sändningar etc.
    </p>

